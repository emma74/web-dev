//Exponential
let expResults = 2 ** 3; //2 raised to the power of 3
console.log("Exponential: " + expResults); // Outputs: 8

//Using Math.pow()
let powerMath = Math.pow(4, 2); //4 raised to the power of 2 using Math.pow()
console.log("Power using Math.pow(): " + powerMath); // Outputs: 16

//incrementing
let count = 5;
count++; //Incrementing by 1
console.log("Incremented Count: " + count); // Outputs: 6

//decrementing
let score = 10;
score--; //Decrementing by 1
console.log("Decremented Score: " + score); // Outputs: 9

//Using ++ operator in prefix form
console.log(--score); // Outputs: 9 then decrements to 8

//Modulus
let remainder = 10 % 3; //Remainder of 10 divided by 3
console.log("Modulus: " + remainder); // Outputs: 1   



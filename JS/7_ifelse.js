//Program flow (conditional statements)
let age = 20

if(age >= 18){
  console.log("You are an adult.");
}else{
  console.log("You are a minor.");
}

//ifelse statement with multiple conditions
let score = 85;

if(score >= 90){
  console.log("Grade: A");
}else if(score >= 80){
  console.log("Grade: B");
}else if(score >= 70){
  console.log("Grade: C");
}else{
  console.log("Grade: D");
}